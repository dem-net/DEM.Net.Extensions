using DEM.Net.Core.Configuration;
using Microsoft.Extensions.Logging;
using Microsoft.Extensions.Options;
using Sketchfab;
using System;
using System.Collections.Generic;
using System.IO;
using System.Linq;
using System.Text;

namespace SampleApp
{
    class SketchfabSample
    {
        private readonly ILogger _logger;
        private readonly Sketchfab.SketchfabApi _sketchFabApi;
        private readonly string _sketchFabToken;

        public SketchfabSample(Sketchfab.SketchfabApi sketchFabExport, ILogger<SketchfabSample> logger, IOptions<AppSecrets> secrets)
        {
            this._logger = logger;
            this._sketchFabApi = sketchFabExport;
            this._sketchFabToken = secrets.Value.SketchFabToken;

            if (string.IsNullOrEmpty(this._sketchFabToken))
            {
                _logger.LogWarning($"SketchfabToken is not set. Ensure you have a secrets.json file with a SketchfabToken entry with your api token (see https://sketchfab.com/settings/password)");
            }
        }

        public void Run()
        {

            List<Collection> collections = _sketchFabApi.GetMyCollectionsAsync(_sketchFabToken, TokenType.Token).GetAwaiter().GetResult();

            if (collections.Any(c => c.name == "Helladic Tests"))
            {

            }


            UploadModelRequest upload = new UploadModelRequest()
            {
                Description = "TEST",// * Generated by [DEM Net Elevation API](https://elevationapi.com)\n* Helladic test upload",
                FilePath = Path.Combine(Directory.GetCurrentDirectory(), "Sketchfab", "C1410_OsmTopo.glb"),
                IsInspectable = true,
                IsPrivate = false,
                IsPublished = false,
                Name = "C1410_OsmTopo TEST",
                Options = new ModelOptions() { Background = SkecthFabEnvironment.Footprint_Court, Shading = ShadingType.lit }
            };


            var uuid = _sketchFabApi.UploadModelAsync(upload, _sketchFabToken).GetAwaiter().GetResult();
        }
    }
}
